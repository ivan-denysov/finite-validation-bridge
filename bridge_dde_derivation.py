"""
МОСТ-5: проверка АНАЛИТИЧЕСКОГО вывода DDE→2nd order.
Вывод дал: (τ²/2)F'·θ̈ + (1+τF')·θ̇ = F(θ) + (τ²/2)F''·θ̇²
Для малой моды θ=A·cos(kx), линеаризация F около 0: F'≈-1-K·(2/a²)(1-cos ka) (отриц.),
F(θ)≈F'·θ. Тогда консервативная часть: (τ²/2)F'·θ̈ = F'·θ => θ̈ = (2/τ²)·θ.
СТОП: это даёт θ̈=(2/τ²)θ с ПОЛОЖИТ. коэф => экспоненту, не осцилляцию. Значит знак/режим тоньше.
Правильно: для ОСЦИЛЛЯЦИЙ нужен эффективный осциллятор m·θ̈ = -k_eff·θ.
m_eff=(τ²/2)|F'|, восстанавливающая = |F'| (от F(θ)=F'θ при F'<0 это -|F'|θ => возврат).
=> ω² = |F'| / ((τ²/2)|F'|) = 2/τ²  => ω=√2/τ => T=2π/ω = 2π τ/√2 = π√2·τ ≈ 4.44·τ.
ПРОВЕРЯЕМ: T/τ должно → π√2 ≈ 4.443 (НЕ зависит от F'! чистое предсказание вывода).
"""
import numpy as np
pred = np.pi*np.sqrt(2)
print(f"Предсказание вывода: T/τ = π√2 = {pred:.4f} (не зависит от жёсткости — чистый тест)")
print()
# численная проверка на DDE первого порядка с запаздыванием, РАЗНЫЕ жёсткости K
def run(tau,K,N=64,a=1.0,dt=0.004,T_end=300):
    steps=int(T_end/dt); lag=max(1,int(round(tau/dt)))
    x=np.arange(N)*a; k0=2*np.pi*2/(N*a)
    th0=1e-3*np.cos(k0*x); buf=np.tile(th0,(lag+1,1)); th=th0.copy()
    def lap(t):return (np.roll(t,-1)+np.roll(t,1)-2*t)/a**2
    ser=[]
    for s in range(steps):
        thd=-np.sin(th)+K*lap(buf[0])   # F(θ(t-τ)); on-site -sinθ мгновенный? нет: всё с лагом
        # честно: всё F с лагом
        thd=-np.sin(buf[0])+K*lap(buf[0])
        th=th+dt*thd; buf=np.roll(buf,-1,axis=0); buf[-1]=th
        if s%2==0: ser.append(th[0])
    ser=np.array(ser)
    sp=np.abs(np.fft.rfft(ser-ser.mean())); fr=np.fft.rfftfreq(len(ser),d=2*dt)
    pk=np.argmax(sp[1:])+1; f=fr[pk]
    return (1/f) if f>0 else np.nan
print(f"{'τ':>5} {'K':>5} {'T_изм':>8} {'T/τ':>8} {'предсказ π√2':>13}")
for tau in [1.0,2.0,3.0]:
    for K in [0.5,1.0,2.0]:
        T=run(tau,K)
        print(f"{tau:>5.1f} {K:>5.1f} {T:>8.2f} {T/tau:>8.3f} {pred:>13.4f}")
print()
print("Если T/τ ≈ π√2 ≈ 4.44 НЕЗАВИСИМО от K — аналитический вывод m_eff=(τ²/2)F' ВЕРЕН:")
print("инерция и её коэффициент выведены из конечности тика, не подогнаны.")
