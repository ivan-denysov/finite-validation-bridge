"""
МОСТ-6: граница применимости sine-Gordon приближения для DDE.
DDE θ̇=-Sθ(t-τ) имеет БЕСКОНЕЧНО много корней λ_n уравнения λ=-S e^{-λτ}.
SG (2-й порядок) удерживает только ГЛАВНУЮ пару. Приближение законно, если высшие корни
затухают много быстрее главного: |Re λ_главный| << |Re λ_следующий|.
Мерим отношение R = Re(λ_2)/Re(λ_1) (во сколько раз быстрее гаснет 2-й корень) как функцию p=Sτ.
Большое R = чисто SG (высшие моды мгновенно дохнут). R→1 = SG-приближение ломается.
Корни через ветви функции Ламберта: λτ = W_k(-Sτ), k=0,±1,±2,... => λ_k = W_k(-p)/τ.
"""
import numpy as np
from scipy.special import lambertw

def roots(p, tau, nbranch=6):
    # λ_k = W_k(-p)/τ ; берём ветви k=0,-1,1,-2,2,... (главные = с наибольшим Re)
    ks=[0,-1,1,-2,2,-3,3]
    lam=[lambertw(-p, k)/tau for k in ks[:nbranch]]
    lam=sorted(lam, key=lambda z: -z.real)  # по убыванию Re (главный первый)
    return lam

print("Граница SG-приближения: R = |Re λ_1 / Re λ_2| (во сколько 2-й корень гаснет быстрее главного)")
print("Большое R => высшие моды задавлены => SG (2 моды) законен. R~1 => нужна полная DDE.")
print(f"{'p=Sτ':>7} {'Re λ1·τ':>9} {'Im λ1·τ':>9} {'Re λ2·τ':>9} {'R=ratio':>9} {'SG?':>10}")
tau=1.0
for p in [0.2,0.5,1.0,1.4,1.5,1.57,2.0,3.0,5.0]:
    lam=roots(p,tau)
    l1=lam[0]; 
    # второй РАЗЛИЧНЫЙ по Re корень
    l2=next((z for z in lam[1:] if abs(z.real-l1.real)>1e-6), lam[1])
    R=abs(l1.real/l2.real) if abs(l2.real)>1e-9 else np.inf
    verdict = 'чистый SG' if R<0.3 else ('SG ок' if R<0.6 else ('погранично' if R<0.9 else 'нужна DDE'))
    print(f"{p:>7.2f} {l1.real:>9.4f} {l1.imag:>9.4f} {l2.real:>9.4f} {R:>9.3f} {verdict:>10}")
print()
print("Интерпретация: R = |Re λ1|/|Re λ2| мал => главная мода живёт ДОЛГО, высшие гаснут быстро => SG.")
print("Порог Хопфа p=π/2≈1.571: Re λ1→0 (главная мода перестаёт затухать) => R→0 => SG-приближение")
print("ИДЕАЛЬНО работает у порога (главная мода чисто осциллирует, высшие задавлены). За порогом — раскачка.")
